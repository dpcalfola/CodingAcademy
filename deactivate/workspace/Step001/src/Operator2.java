public class Operator2 {
    public static void main(String[] args) {
        int n = 13;

        System.out.println(n += 4);
        System.out.println(n -= 4);
        System.out.println(n *= 4);
        System.out.println(n /= 4);
        System.out.println(n %= 4);
        System.out.println(n);

        // 복합대입연산자
    }
}
