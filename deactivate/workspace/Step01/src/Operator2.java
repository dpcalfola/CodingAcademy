public class Operator2 {

    public static void main(String[] args) {
        int n1 = 10, n2 = 7;
        int result;

        result = n1 + n2;
        System.out.println("+ :" + result);

        result = n1 - n2;
        System.out.println("- :" + result);

        result = n1 * n2;
        System.out.println("* :" + result);

        result = n1 / n2;  // 결과: 몫
        System.out.println("/ :" + result);

        result = n1 % n2;  // 결과: 나머지
        System.out.println("% :" + result);
    }
}
