public class StringPlus {
    public static void main(String[] args) {

        // 정수형 변수 선언
        int num = 100 ;


        System.out.println("Hello " + "World"); // 문자열과 문자열을 더하면 하나로 합쳐준다.
        System.out.println("num : " + num);
        System.out.println("result : " +1+2+3+4); // 문자열에 숫자를 더하면 문자열로 변환
        System.out.println("result : " +(1+2+3+4)); // 괄호를 이용하여 숫자를 먼저 계산

        // 계산은 항항 왼쪽에서부터

        System.out.println(1+2);



    }
}
