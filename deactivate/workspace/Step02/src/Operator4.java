public class Operator4 {

    /*
     *
     * 관계 연산자
     *   > , <,  >=, <=, == !=
     *
     *
     * */

    public static void main(String[] args) {

        // 정수형 변수 두개 선언 및 초기화

        int a, b;
        a = 27;
        b = 599;

        System.out.println( a < b);
        System.out.println( a <= b);
        System.out.println( a > b);
        System.out.println( a >= b);
        System.out.println( a == b);
        System.out.println( a != b);





    }
}
